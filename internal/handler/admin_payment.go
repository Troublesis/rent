func paymentToAPIItem(payment model.Payment, now time.Time) paymentAPIItem {
	today := paymentAPIDateOnly(now)
	payDate := paymentAPIDateOnly(payment.PayDate)
	paymentStatus := "unpaid"
	if payment.Paid {
		paymentStatus = "paid"
	}
	overdue := !payment.Paid && !payment.Excluded && !payDate.IsZero() && payDate.Before(today)
	overdueDays := 0
	if overdue {
		overdueDays = int(today.Sub(payDate).Hours() / 24)
	}
	nextPayDateLabel := formatAPIDate(payment.PayDate)
	if model.RentTypeOrDefault(payment.Tenant.RentType) == model.RentTypeDaily {
		nextPayDateLabel = "每日"
	}
	return paymentAPIItem{
		ID:                payment.ID,
		PaymentID:         payment.ID,
		TenantID:          payment.TenantID,
		TenantName:        payment.Tenant.Name,
		RoomNo:            payment.Tenant.Room.RoomNo,
		Room:              payment.Tenant.Room.RoomNo,
		TenantStatus:      payment.Tenant.Status,
		Phone:             payment.Tenant.Phone,
		RentPriceFen:      payment.Tenant.RentPrice,
		RentPriceText:     service.FormatFenAsYuanInt(payment.Tenant.RentPrice),
		RentType:          payment.Tenant.RentType,
		RentTypeLabel:     rentTypeLabelText(payment.Tenant.RentType),
		PaymentTerms:      payment.Tenant.PaymentTerms,
		PaymentTermsLabel: paymentTermsLabelText(payment.Tenant.PaymentTerms),
		CheckinDate:       formatAPIDate(payment.Tenant.CheckinDate),
		LeaseEndDate:      formatOptionalAPIDate(payment.Tenant.LeaseEndDate),
		PayDate:           formatAPIDate(payment.PayDate),
		NextPayDateLabel:  nextPayDateLabel,
		Type:              payment.Type,
		TypeLabel:         paymentTypeLabelText(payment.Type),
		AmountFen:         payment.Amount,
		AmountText:        service.FormatFen(payment.Amount),
		Paid:              payment.Paid,
		PaymentStatus:     paymentStatus,
		StatusLabel:       paymentStatusLabelText(payment.Paid),
		Excluded:          payment.Excluded,
		ExclusionNote:     payment.ExclusionNote,
		AutoGenerated:     payment.AutoGenerated,
		Overdue:           overdue,
		OverdueDays:       overdueDays,
		OverdueLabel:      overdueLabelText(overdueDays),
		Note:              payment.Note,
	}
}

func queryInt(c *gin.Context, name string) int {
	value := strings.TrimSpace(c.Query(name))
	if value == "" {
		return 0
	}
	parsed, err := strconv.Atoi(value)
	if err != nil {
		return 0
	}
	return parsed
}

func boolQuery(value string) *bool {
	switch value {
	case "true":
		parsed := true
		return &parsed
	case "false":
		parsed := false
		return &parsed
	default:
		return nil
	}
}

func paidQuery(c *gin.Context) *bool {
	value, exists := c.GetQuery("paid")
	if !exists {
		parsed := false
		return &parsed
	}
	return boolQuery(value)
}

func paidFilterValue(c *gin.Context) string {
	value, exists := c.GetQuery("paid")
	if !exists {
		return "false"
	}
	switch value {
	case "all", "true":
		return value
	default:
		return "false"
	}
}

func excludedQuery(value string) *bool {
	switch value {
	case "all":
		return nil
	case "true":
		parsed := true
		return &parsed
	default:
		parsed := false
		return &parsed
	}
}

func excludedFilterValue(value string) string {
	switch value {
	case "all", "true":
		return value
	default:
		return "false"
	}
}

func dateQuery(value string) (time.Time, bool) {
	value = strings.TrimSpace(value)
	if value == "" {
		return time.Time{}, false
	}
	parsed, err := time.ParseInLocation("2006-01-02", value, time.Local)
	if err != nil {
		return time.Time{}, false
	}
	return parsed, true
}

func normalizePaymentPage(page int) int {
	if page < 1 {
		return 1
	}
	return page
}

func normalizePaymentLimit(limit int) int {
	if limit <= 0 {
		return 20
	}
	if limit > 100 {
		return 100
	}
	return limit
}

func paymentAPIDateOnly(value time.Time) time.Time {
	if value.IsZero() {
		return time.Time{}
	}
	return time.Date(value.Year(), value.Month(), value.Day(), 0, 0, 0, 0, value.Location())
}

// formatAPIDate emits ISO yyyy-MM-dd for JSON API consumers. JavaScript on the
// frontend reformats it into the Chinese display style via formatDisplayDate.
func formatAPIDate(value time.Time) string {
	if value.IsZero() {
		return ""
	}
	return value.Format("2006-01-02")
}

func formatOptionalAPIDate(value *time.Time) string {
	if value == nil || value.IsZero() {
		return ""
	}
	return value.Format("2006-01-02")
}

// formatDisplayDate renders a Chinese-style date used for server-rendered
// labels (e.g., "2026年5月23日"). Returns empty string for zero values.
func formatDisplayDate(value time.Time) string {
	if value.IsZero() {
		return ""
	}
	return fmt.Sprintf("%d年%d月%d日", value.Year(), int(value.Month()), value.Day())
}

func overdueLabelText(days int) string {
	if days <= 0 {
		return ""
	}
	return "逾期 " + strconv.Itoa(days) + " 天"
}

func paymentStatusLabelText(paid bool) string {
	if paid {
		return "已付款"
	}
	return "未付款"
}

func paymentTypeLabelText(paymentType string) string {
	switch paymentType {
	case model.PaymentTypeRent:
		return "租金"
	case model.PaymentTypeWater:
		return "水费"
	case model.PaymentTypeElectricity:
		return "电费"
	case model.PaymentTypeOther:
		return "其他"
	default:
		return "未知"
	}
}

func rentTypeLabelText(rentType string) string {
	if model.RentTypeOrDefault(rentType) == model.RentTypeDaily {
		return "日租"
	}
	return "月租"
}

func paymentTermsLabelText(paymentTerms string) string {
	switch model.PaymentTermsOrDefault(paymentTerms) {
	case model.PaymentTerms1M2D:
		return "付一押二"
	case model.PaymentTerms3M1D:
		return "付三押一"
	case model.PaymentTerms6M0D:
		return "半年付"
	case model.PaymentTerms12M0D:
		return "年付"
	default:
		return "付一押一"
	}
}
