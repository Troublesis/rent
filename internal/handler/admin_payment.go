const (
	adminPaymentViewList = "list"
	adminPaymentViewGrid = "grid"
	adminPaymentViewCard = "card"
)

func adminPaymentViewFromQuery(c *gin.Context) string {
	switch c.Query("view") {
	case adminPaymentViewGrid:
		return adminPaymentViewGrid
	case adminPaymentViewCard:
		return adminPaymentViewCard
	default:
		return adminPaymentViewList
	}
}

func paymentListURL(c *gin.Context, overrides map[string]string) string {
	values := url.Values{}
	for key, vals := range c.Request.URL.Query() {
		for _, value := range vals {
			values.Add(key, value)
		}
	}
	for key, value := range overrides {
		if value == "" {
			values.Del(key)
			continue
		}
		values.Set(key, value)
	}
	encoded := values.Encode()
	if encoded == "" {
		return "/admin/payments"
	}
	return "/admin/payments?" + encoded
}

func paymentClearURL(viewMode string) string {
	if viewMode == "" {
		return "/admin/payments"
	}
	return "/admin/payments?view=" + url.QueryEscape(viewMode)
}

func paymentSortValue(filter repository.PaymentFilter) string {
	sortBy := filter.SortBy
	if sortBy == "" {
		sortBy = "next_pay_date"
	}
	sortDir := filter.SortDir
	if sortDir == "" {
		sortDir = "asc"
	}
	return sortBy + ":" + sortDir
}

func paymentRedirectTarget(c *gin.Context) string {
	target := c.PostForm("return_to")
	if strings.HasPrefix(target, "/admin/payments") {
		return target
	}
	return "/admin/payments"
}

func (h *AdminPaymentHandler) List(c *gin.Context) {
	data, err := h.buildPaymentListData(c)
	if err != nil {
		c.String(http.StatusInternalServerError, err.Error())
		return
	}
	if isHTMXRequest(c) {
		h.renderer.RenderPartial(c, http.StatusOK, "admin/payments.html", "admin_payment_list", data)
		return
	}
	h.renderer.Render(c, http.StatusOK, "admin_base.html", "admin/payments.html", data)
}

func (h *AdminPaymentHandler) buildPaymentListData(c *gin.Context) (gin.H, error) {
	now := time.Now()
	if _, err := h.paymentService.GenerateDueRecordsForActiveTenants(now); err != nil {
		return nil, errPaymentGenerate
	}
	filter := paymentFilterFromQuery(c)
	if filter.Limit <= 0 {
		filter.Limit = 20
	}
	viewMode := adminPaymentViewFromQuery(c)
	result, err := h.paymentService.ListPaymentsPage(filter, now)
	if err != nil {
		return nil, errPaymentList
	}
	summary, err := h.paymentService.SummarizePayments(filter, now)
	if err != nil {
		return nil, errPaymentSummary
	}
	tenants, err := h.tenantService.ListTenants(repository.TenantFilter{Status: model.TenantStatusActive})
	if err != nil {
		return nil, errPaymentTenants
	}
	rows := make([]paymentRow, len(result.Payments))
	for i, payment := range result.Payments {
		rows[i] = paymentToRow(payment, now)
	}
	page := normalizePaymentPage(filter.Page)
	limit := normalizePaymentLimit(filter.Limit)
	hasMore := int64(page*limit) < result.Total
	nextPageURL := ""
	if hasMore {
		nextPageURL = paymentListURL(c, map[string]string{"page": strconv.Itoa(page + 1)})
	}
	return gin.H{
		"Title":              "收款记录",
		"Rows":               rows,
		"Summary":            paymentSummaryToAPI(summary),
		"Tenants":            tenants,
		"Filter":             filter,
		"FilterPaid":         paidFilterValue(c),
		"FilterExcluded":     excludedFilterValue(c.Query("excluded")),
		"PaymentTypes":       paymentTypeOptions(),
		"ViewMode":           viewMode,
		"ListViewURL":        paymentListURL(c, map[string]string{"view": adminPaymentViewList}),
		"GridViewURL":        paymentListURL(c, map[string]string{"view": adminPaymentViewGrid}),
		"CardViewURL":        paymentListURL(c, map[string]string{"view": adminPaymentViewCard}),
		"ClearFilterURL":     paymentClearURL(viewMode),
		"SummaryScopes":      paymentSummaryScopes(c),
		"SelectedSort":       paymentSortValue(filter),
		"PaidChips":          paymentPaidChips(c),
		"PeriodChips":        paymentPeriodChips(c),
		"TypeChips":          paymentTypeChips(c),
		"ExcludedChips":      paymentExcludedChips(c),
		"TenantStatusChips":  paymentTenantStatusChips(c),
		"SortHeaders":        paymentSortHeaders(c, filter),
		"Page":               page,
		"HasMore":            hasMore,
		"NextPageURL":        nextPageURL,
		"Total":              result.Total,
		"Error":              queryError(c),
	}, nil
}

func isHTMXRequest(c *gin.Context) bool {
	return c.GetHeader("HX-Request") == "true"
}

// hydrateQueryFromHXCurrentURL copies the query string from the HTMX
// HX-Current-URL header onto the inbound request, so that subsequent
// c.Query(...) calls reflect the filter context the user is currently viewing.
// Without this, POST actions like /payments/:id/toggle would lose the active
// filters and the rebuilt partial would snap back to the default view.
func hydrateQueryFromHXCurrentURL(c *gin.Context) {
	raw := c.GetHeader("HX-Current-URL")
	if raw == "" {
		return
	}
	parsed, err := url.Parse(raw)
	if err != nil {
		return
	}
	c.Request.URL.RawQuery = parsed.RawQuery
}

