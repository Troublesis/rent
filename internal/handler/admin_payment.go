func paymentToRow(payment model.Payment, now time.Time) paymentRow {
	today := paymentAPIDateOnly(now)
	payDate := paymentAPIDateOnly(payment.PayDate)
	overdue := !payment.Paid && !payment.Excluded && !payDate.IsZero() && payDate.Before(today)
	overdueDays := 0
	if overdue {
		overdueDays = int(today.Sub(payDate).Hours() / 24)
	}
	nextLabel := formatDisplayDate(payment.PayDate)
	if model.RentTypeOrDefault(payment.Tenant.RentType) == model.RentTypeDaily {
		nextLabel = "每日"
	}
	return paymentRow{
		ID:                payment.ID,
		TenantID:          payment.TenantID,
		TenantName:        payment.Tenant.Name,
		RoomNo:            payment.Tenant.Room.RoomNo,
		RoomTitle:         payment.Tenant.Room.Title,
		TenantStatus:      payment.Tenant.Status,
		TenantStatusLabel: tenantStatusLabelDisplay(payment.Tenant.Status),
		Type:              payment.Type,
		TypeLabel:         paymentTypeLabelText(payment.Type),
		AmountFen:         payment.Amount,
		AmountText:        service.FormatFen(payment.Amount),
		PayDate:           payment.PayDate,
		PayDateLabel:      formatDisplayDate(payment.PayDate),
		NextDueLabel:      nextLabel,
		Paid:              payment.Paid,
		StatusLabel:       paymentStatusLabelText(payment.Paid),
		Excluded:          payment.Excluded,
		ExclusionNote:     payment.ExclusionNote,
		AutoGenerated:     payment.AutoGenerated,
		Overdue:           overdue,
		OverdueDays:       overdueDays,
		OverdueLabel:      overdueLabelText(overdueDays),
		Note:              payment.Note,
		RentTypeLabel:     rentTypeLabelText(payment.Tenant.RentType),
	}
}

func tenantStatusLabelDisplay(status string) string {
	switch status {
	case model.TenantStatusActive:
		return "在租"
	case model.TenantStatusCheckout:
		return "已退租"
	default:
		return "未知"
	}
}

func paymentChip(label string, c *gin.Context, key string, value string, active bool) filterChip {
	overrides := map[string]string{key: value, "page": ""}
	return filterChip{Label: label, URL: paymentListURL(c, overrides), Active: active}
}

type paymentSummaryScope struct {
	URL    string
	Active bool
}

func paymentSummaryScopes(c *gin.Context) map[string]paymentSummaryScope {
	paid := paidFilterValue(c)
	excluded := excludedFilterValue(c.Query("excluded"))
	tenantStatus := strings.TrimSpace(c.Query("tenant_status"))
	period := strings.TrimSpace(c.Query("period"))
	typeFilter := strings.TrimSpace(c.Query("type"))

	unpaidActive := paid == "false" && excluded == "false" && tenantStatus != model.TenantStatusCheckout
	paidActive := paid == "true" && excluded == "false"
	excludedActive := excluded == "true"
	checkoutActive := tenantStatus == model.TenantStatusCheckout && excluded == "false" && paid == "false"
	_ = period
	_ = typeFilter

	return map[string]paymentSummaryScope{
		"unpaid": {
			URL:    paymentListURL(c, map[string]string{"paid": "false", "excluded": "false", "tenant_status": "", "page": ""}),
			Active: unpaidActive,
		},
		"paid": {
			URL:    paymentListURL(c, map[string]string{"paid": "true", "excluded": "false", "tenant_status": "", "page": ""}),
			Active: paidActive,
		},
		"checkout": {
			URL:    paymentListURL(c, map[string]string{"paid": "false", "excluded": "false", "tenant_status": model.TenantStatusCheckout, "page": ""}),
			Active: checkoutActive,
		},
		"excluded": {
			URL:    paymentListURL(c, map[string]string{"paid": "all", "excluded": "true", "tenant_status": "", "page": ""}),
			Active: excludedActive,
		},
	}
}

