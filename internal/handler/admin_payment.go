func paymentSortHeaders(c *gin.Context, filter repository.PaymentFilter) []sortHeader {
	sortBy := filter.SortBy
	if sortBy == "" {
		sortBy = "next_pay_date"
	}
	sortDir := filter.SortDir
	if sortDir == "" {
		sortDir = "asc"
	}
	headers := []sortHeader{
		{Key: "tenant", Label: "租客"},
		{Key: "next_pay_date", Label: "租金月份"},
		{Key: "amount", Label: "金额"},
		{Key: "status", Label: "付款状态"},
	}
	for i, h := range headers {
		active := h.Key == sortBy
		nextDir := "asc"
		indicator := ""
		if active {
			if sortDir == "asc" {
				nextDir = "desc"
				indicator = "↑"
			} else {
				nextDir = "asc"
				indicator = "↓"
			}
		}
		overrides := map[string]string{"sort_by": h.Key, "sort_dir": nextDir, "page": ""}
		headers[i].URL = paymentListURL(c, overrides)
		headers[i].Active = active
		headers[i].Indicator = indicator
	}
	return headers
}

func (h *AdminPaymentHandler) APIList(c *gin.Context) {
	now := time.Now()
	if _, err := h.paymentService.GenerateDueRecordsForActiveTenants(now); err != nil {
		c.JSON(http.StatusInternalServerError, gin.H{"error": "生成应收记录失败"})
		return
	}
	filter := paymentFilterFromQuery(c)
	result, err := h.paymentService.ListPaymentsPage(filter, now)
	if err != nil {
		c.JSON(http.StatusInternalServerError, gin.H{"error": "读取收款记录失败"})
		return
	}
	summary, err := h.paymentService.SummarizePayments(filter, now)
	if err != nil {
		c.JSON(http.StatusInternalServerError, gin.H{"error": "读取收款汇总失败"})
		return
	}
	page := normalizePaymentPage(filter.Page)
	limit := normalizePaymentLimit(filter.Limit)
	items := make([]paymentAPIItem, len(result.Payments))
	for i, payment := range result.Payments {
		items[i] = paymentToAPIItem(payment, now)
	}
	c.JSON(http.StatusOK, paymentAPIResponse{
		Data:    items,
		Summary: paymentSummaryToAPI(summary),
		Page:    page,
		Limit:   limit,
		Total:   result.Total,
		HasMore: int64(page*limit) < result.Total,
	})
}

func (h *AdminPaymentHandler) Create(c *gin.Context) {
	input, err := paymentInputFromForm(c)
	if err != nil {
		h.respondAfterPaymentCreate(c, "表单数据不正确")
		return
	}
	if _, err := h.paymentService.RecordPayment(input); err != nil {
		h.respondAfterPaymentCreate(c, userFacingError(err))
		return
	}
	h.respondAfterPaymentCreate(c, "")
}

func (h *AdminPaymentHandler) respondAfterPaymentCreate(c *gin.Context, errMsg string) {
	if isHTMXRequest(c) {
		hydrateQueryFromHXCurrentURL(c)
		data, err := h.buildPaymentListData(c)
		if err != nil {
			c.String(http.StatusInternalServerError, err.Error())
			return
		}
		if errMsg != "" {
			data["Error"] = errMsg
		} else {
			c.Header("HX-Trigger", "payment-created")
		}
		h.renderer.RenderPartial(c, http.StatusOK, "admin/payments.html", "admin_payment_list", data)
		return
	}
	if errMsg != "" {
		redirectWithError(c, "/admin/payments", errMsg)
		return
	}
	c.Redirect(http.StatusSeeOther, "/admin/payments")
}

func (h *AdminPaymentHandler) Toggle(c *gin.Context) {
	id, ok := parseUintParam(c, "id")
	if !ok {
		return
	}
	if err := h.paymentService.TogglePaid(id); err != nil {
		h.respondAfterPaymentAction(c, userFacingError(err))
		return
	}
	h.respondAfterPaymentAction(c, "")
}

func (h *AdminPaymentHandler) UpdateExclusion(c *gin.Context) {
	id, ok := parseUintParam(c, "id")
	if !ok {
		return
	}
	excluded := c.PostForm("excluded") == "true"
	if err := h.paymentService.SetExcluded(id, excluded, c.PostForm("exclusion_note")); err != nil {
		h.respondAfterPaymentAction(c, userFacingError(err))
		return
	}
	h.respondAfterPaymentAction(c, "")
}

func (h *AdminPaymentHandler) respondAfterPaymentAction(c *gin.Context, errMsg string) {
	if isHTMXRequest(c) {
		hydrateQueryFromHXCurrentURL(c)
		data, err := h.buildPaymentListData(c)
		if err != nil {
			c.String(http.StatusInternalServerError, err.Error())
			return
		}
		if errMsg != "" {
			data["Error"] = errMsg
		}
		h.renderer.RenderPartial(c, http.StatusOK, "admin/payments.html", "admin_payment_list", data)
		return
	}
	target := paymentRedirectTarget(c)
	if errMsg != "" {
		redirectWithError(c, target, errMsg)
		return
	}
	c.Redirect(http.StatusSeeOther, target)
}

func (h *AdminPaymentHandler) Exclude(c *gin.Context) {
	id, ok := parseUintParam(c, "id")
	if !ok {
		return
	}
	var input paymentExclusionRequest
	if err := c.ShouldBindJSON(&input); err != nil {
		c.JSON(http.StatusBadRequest, gin.H{"error": "请求数据不正确"})
		return
	}
	note := input.ExclusionNote
	if strings.TrimSpace(note) == "" {
		note = input.Note
	}
	if err := h.paymentService.SetExcluded(id, input.Excluded, note); err != nil {
		c.JSON(http.StatusBadRequest, gin.H{"error": userFacingError(err)})
		return
	}
	c.JSON(http.StatusOK, gin.H{"success": true})
}

func paymentFilterFromQuery(c *gin.Context) repository.PaymentFilter {
	filter := repository.PaymentFilter{
		Type:         c.Query("type"),
		Query:        c.Query("q"),
		Period:       c.Query("period"),
		TenantStatus: tenantStatusFilterValue(c.Query("tenant_status")),
		Page:         queryInt(c, "page"),
		Limit:        queryInt(c, "limit"),
		SortBy:       c.Query("sort_by"),
		SortDir:      c.Query("sort_dir"),
	}
	filter.Paid = paidQuery(c)
	filter.Excluded = excludedQuery(c.Query("excluded"))
	filter.Overdue = boolQuery(c.Query("overdue"))
	if tenantID, err := strconv.ParseUint(c.Query("tenant_id"), 10, 64); err == nil {
		filter.TenantID = uint(tenantID)
		// A direct tenant pick from the dropdown is authoritative; the human-
		// readable label still in `q` would otherwise LIKE-match nothing and
		// AND the result down to zero rows.
		if filter.TenantID > 0 {
			filter.Query = ""
		}
	}
	if fromDate, ok := dateQuery(c.Query("from")); ok {
		filter.FromDate = fromDate
	}
	if toDate, ok := dateQuery(c.Query("to")); ok {
		filter.ToDate = toDate
	}
	return filter
}

func paymentInputFromForm(c *gin.Context) (service.PaymentInput, error) {
	input := service.PaymentInput{
		AmountYuan: c.PostForm("amount"),
		Type:       c.PostForm("type"),
		Paid:       c.PostForm("paid") == "on",
		Note:       c.PostForm("note"),
	}
	tenantID, err := parseUintForm(c, "tenant_id")
	if err != nil {
		return input, err
	}
	input.TenantID = tenantID
	payDate, err := parseDateForm(c, "pay_date")
	if err != nil {
		return input, err
	}
	input.PayDate = payDate
	return input, nil
}

